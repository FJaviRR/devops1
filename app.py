"""Módulo para realizar operaciones aritméticas básicas."""


def suma(a_val: int, b_val: int) -> int:
    """
    Calcula la suma de dos números enteros.

    Args:
        a_val (int): El primer número.
        b_val (int): El segundo número.

    Returns:
        int: La suma de a y b.
    """
    return a_val + b_val